void ainr_ulaw_decode(uint8_t *input, uint16_t *output, size_t len)
{
	for (size_t i = 0; i < len; i++) {
		output[i] = ulaw_decode(input[i]);
	}
}


extern void ulaw_encode_asm_16(uint16_t *input, uint8_t *output, size_t len, const uint8_t *lut);
void ainr_ulaw_encode_fast(uint16_t *input, uint8_t *output, size_t len)
{
	size_t n = len;

	if (n >= 16) {
		size_t count_16 = n & ~15;
		ulaw_encode_asm_16(input, output, count_16, ulaw_encode_lut);

		input += count_16;
		output += count_16;
		n -= count_16;
	}

	while (n--) {
		*output++ = ulaw_encode_lut[*input++];
	}
}


